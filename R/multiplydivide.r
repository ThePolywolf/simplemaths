#' multiplies 2 numbers and divides by a third
#' @description This function takes in 3 numbers, multiplies the first two and divides by the third
#' @param m1 is the first number in the multiplication
#' @param m2 is the second number in the multiplication
#' @param d1 is the divisor in the function
#' @example multiplydivide(4, 5, 2)
#' @export 
multiplydivide <- function(m1, m2, d1) {
  m1 * m2 / d1
}