#' An example 3 parts addition function
#' @description This function takes three numbers and combines them using addition
#' @param n1 the first number to be added
#' @param n2 the second number to be added
#' @param n3 the third number to be added
#' @example add3(5, 6, 7)
#' @export
add3 <- function(n1, n2, n3){
  n1 + n2 + n3
}
